                <h2 class="section-title">Challenges</h2>
                <p class="mt-4">One of the main challenges for the app was the feed. I was faced by the opposition of keeping a clean feed that entices the user to read posts, while keeping each post short and simple to allow for more posts to be seen on the screen at one time.
                There was also the added complexity of having all of the controls relating to a user as well as the post itself.
                </p>
                <p class="mt-4">
                The solution was a clean card preview for each post that included the title, preview of content, the first image attached to the post as a backdrop, the author, post type and ellipses that expand to be controls.
                There are three different types of posts and I wanted the user to realise this at a glance, hence the different coloured overlays on each post type.
                <p/>
                <p class="mt-4">
                I included the swipe functionality to remove unwanted posts from the feed and added a modal to confirm the users choice as this is quite a destructive action.
                </p>
