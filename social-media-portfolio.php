                <h1 class="section-title">Research</h1>
                <p>When designing the screens and flow of the application, Instagram and Facebook were my top 2 inspirations. Millions of people use at least one of these apps on a daily basis and have become familiar to how it works, there’s no need to reinvent the wheel when it comes to simple and important components such as the navigation bars.  <br/>
The bottom navigation bar for my project included quick links to the top 5 areas of the app and was always visible on the screen except for the messaging area.
This was to allow extra space for users to view the chat as it’s frustrating to only see a couple of messages at a time and extra screen space becomes valuable at this stage.</p>                
