                <h1 class="section-title">Adaptation</h1>
                <p>I adapted my designs to the app requirements as it evolved. A great example for this is the interests page that was originally only 10 choices. Towards the end of development, this changed to 40+ choices which required a review of the design.
I outsourced icons and modified them to fit in with the brand guidelines and created a container where users can scroll to select their interests.
I made sure to have the icons slightly cut off towards the end of the container and added a textual prompt so that users are aware of the number of choices available to them.</p>                
