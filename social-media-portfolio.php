                <h2 class="section-title">Storyboarding</h2>
                <p>During the early stages of the project, I carried out storyboarding sessions with the client to give them an idea about the user flow and the main elements on the screen to gather feedback from them.<br/>s
During our first call, I focused on the 3 main flows: Signup, Create Post & Interacting with posts. Once the client was happy with these I moved on to the secondary flows. The process was very agile and the client was included in the decisions at every stage.
Once they were happy with the flow, we moved on to the XD designs where I used the mood boards provided by the client as inspiration. 
I presented them with several design iterations until they were happy with the final choice.</p>
